// Programa que suma los números del 0 al 10 usando ciclos

#include <iostream>

using namespace std;

int main(){

	//Variable de control de ciclo
	int control = 0;

	//Variable que acumula los resultados parciales de la suma
	int acumulador = 0;

	while(control <= 10){ //Abre ciclo mientras

		/* 
		La variable acumuladora almacena los resultados parciales de la variable
		de control de ciclo. 
		acumulador += control equivale a: acumulador = acumulador + control
		*/
		acumulador += control; 

		/* 
		Incremento de la variable de control
		Equivalente a: numero = numero + 1 
		*/
		control++; 
	} // Cierra ciclo mientras

	//Mostrar en pantalla el resultado

	cout << "Resultado de la suma de los numeros del 0 al 10: " << acumulador << endl;

	cin.get();

	return 0;
}